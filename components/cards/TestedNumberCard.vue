<template>
  <v-col cols="12" md="6" class="DataCard">
    <client-only>
      <time-stacked-bar-chart
        :title="$t('検査実施件数')"
        :title-id="'number-of-tested'"
        :chart-id="'time-stacked-bar-chart-inspections'"
        :chart-data="inspectionsGraph"
        :date="Data.inspections_summary.date"
        :items="inspectionsItems"
        :labels="inspectionsLabels"
        :unit="$t('件.tested')"
        :data-labels="inspectionsDataLabels"
        :table-labels="inspectionsTableLabels"
      >
        <!-- 件.tested = 検査数 -->
        <template v-slot:additionalDescription>
          <span>{{ $t('（注）') }}</span>
          <ul>
            <li>
              {{
                $t(
                  '速報値として公開するものであり、後日確定データとして修正される場合がある'
                )
              }}
            </li>
          </ul>
        </template>
      </time-stacked-bar-chart>
    </client-only>
  </v-col>
</template>

<script>
import dayjs from 'dayjs'
import duration from 'dayjs/plugin/duration'
import Data from '@/data/data.json'
import { getDayjsObject } from '@/utils/formatDate'
import TimeStackedBarChart from '@/components/TimeStackedBarChart.vue'
dayjs.extend(duration)

export default {
  components: {
    TimeStackedBarChart,
  },
  data() {
    // 検査実施日別状況
    const { data } = Data.inspections_summary
    const pcr = Array.from(data['PCR検査'].keys()).map(
      (i) => data['PCR検査'][i]
    )
    const antigen = Array.from(data['抗原検査'].keys()).map(
      (i) => data['抗原検査'][i]
    )
    const inspectionsGraph = [pcr, antigen]
    const inspectionsItems = [
      this.$t('PCR検査実施件数'),
      this.$t('抗原検査件数'),
    ]
    const inspectionsLabels = Data.inspections_summary.labels.map((d) => {
      return getDayjsObject(d).format('YYYY-MM-DD')
    })
    const inspectionsDataLabels = [
      this.$t('PCR検査実施件数'),
      this.$t('抗原検査件数'),
    ]
    const inspectionsTableLabels = [
      this.$t('PCR検査実施件数'),
      this.$t('抗原検査件数'),
    ]

    return {
      Data,
      inspectionsGraph,
      inspectionsItems,
      inspectionsLabels,
      inspectionsDataLabels,
      inspectionsTableLabels,
    }
  },
}
</script>
